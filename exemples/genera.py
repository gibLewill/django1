import random


def generate_phone_number():
    """
    Génère un numéro de téléphone de 9 chiffres :
    - Le premier est toujours '6'
    - Les huit suivants sont compris entre 2 et 9
    """
    return '6' + ''.join(str(random.randint(2, 9)) for _ in range(8))

def main():
    # Génère et affiche 20 numéros de téléphone
    for _ in range(20):
        print(generate_phone_number())

